
public class QuocGia {
	private String maQg;
	private String tenQg;
	
	public QuocGia(String maQg, String tenQg) {
		this.maQg = maQg;
		this.tenQg = tenQg;
	}

	public String getMaQg() {
		return maQg;
	}

	public void setMaQg(String maQg) {
		this.maQg = maQg;
	}

	public String getTenQg() {
		return tenQg;
	}

	public void setTenQg(String tenQg) {
		this.tenQg = tenQg;
	}
	
}
