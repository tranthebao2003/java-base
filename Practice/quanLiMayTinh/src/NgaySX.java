
public class NgaySX {
	private int ngay, thang,nam;

	public NgaySX(int ngay, int thang, int namg) {
		super();
		this.ngay = ngay;
		this.thang = thang;
		this.nam = namg;
	}

	public int getNgay() {
		return ngay;
	}

	public void setNgay(int ngay) {
		this.ngay = ngay;
	}

	public int getThang() {
		return thang;
	}

	public void setThang(int thang) {
		this.thang = thang;
	}

	public int getNamg() {
		return nam;
	}

	public void setNamg(int nam) {
		this.nam = nam;
	}
	
	
}
